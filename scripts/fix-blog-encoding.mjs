function escapeRe(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

for (const f of fs.readdirSync(dir).filter((x) => x.endsWith(".jsx") && x !== "blog-shared.jsx")) {
    let s = fs.readFileSync(path.join(dir, f), "utf8");
    for (const [a, b] of map) {
        s = s.replace(new RegExp(escapeRe(a), "g"), b);
    }
    if (s.includes("useEffect(") && !/import React[^;]*useEffect/.test(s)) {
        s = s.replace(/import React(?:, \{([^}]+)\})? from "react";/, (m, g1) => {
            const parts = (g1 || "").split(",").map((x) => x.trim()).filter(Boolean);
            if (!parts.includes("useEffect")) parts.push("useEffect");
            return `import React, { ${parts.join(", ")} } from "react";`;
        });
    }
    fs.writeFileSync(path.join(dir, f), s, "utf8");
    const bad = (s.match(/\uFFFD/g) || []).length;
    console.log(`${f}: replacement chars left=${bad}, useEffect import=${/useEffect/.test(s.match(/import[^;]+react/)?.[0] || "")}`);
}
